echo "âœ… Development environment started!"
echo "ðŸ“¡ Backend: http://localhost:8787"
echo "ðŸ–¥ï¸  Frontend: http://localhost:5173"
echo "ðŸ“‹ Admin Panel: http://localhost:5173/#/admin"
echo "ðŸ”§ Mock Auth: http://localhost:5173/#/dev"
