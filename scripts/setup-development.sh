if [ -f ".gitignore" ]; then
    # Add development-specific entries if not already present
    if ! grep -q ".dev.vars" .gitignore; then
        echo "" >> .gitignore
        echo "# Development environment" >> .gitignore
        echo ".dev.vars" >> .gitignore
