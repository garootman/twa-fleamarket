# Wait for worker to start
sleep 3

# Start frontend (if webapp directory exists)
if [ -d "webapp" ]; then
    echo "ðŸ–¥ï¸  Starting frontend..."
    cd webapp
    npm run dev &
    FRONTEND_PID=$!
    cd ..
